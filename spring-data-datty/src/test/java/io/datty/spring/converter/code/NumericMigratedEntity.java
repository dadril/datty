package io.datty.spring.converter.code;

import org.springframework.data.annotation.Id;

import io.datty.spring.mapping.Entity;
import io.datty.spring.mapping.DattyProperty;

/**
 * NumericMigratedEntity
 * 
 * @author Alex Shvid
 *
 */

@Entity(setName="TEST_CACHE")
public class NumericMigratedEntity {

	@Id
	@DattyProperty(code=1)
	private Long id;
	
	@DattyProperty(code=2)
	private String first;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getFirst() {
		return first;
	}

	public void setFirst(String first) {
		this.first = first;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((first == null) ? 0 : first.hashCode());
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		NumericMigratedEntity other = (NumericMigratedEntity) obj;
		if (first == null) {
			if (other.first != null)
				return false;
		} else if (!first.equals(other.first))
			return false;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "TaggedMigratedCrossEntity [id=" + id + ", first=" + first + "]";
	}

	
}
